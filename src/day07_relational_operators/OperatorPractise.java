package day07_relational_operators;

public class OperatorPractise {
    public static void main (String [] args){

        System.out.println(-5 + 3);

        int b = 10;
        b++; // b + 1 = 11
        System.out.println(b); // 11

        ++b; // 1 + b = 11
        System.out.println(b); // 12

        b--; // b - 1 = 11
        System.out.println(b); // 11

        --b; // b - 1 = 10
        System.out.println(b); // 10

        System.out.println(b++); // 10

        System.out.println(++b); // 12

        System.out.println(b--); // 12

        System.out.println(b); // 11
    }
}
